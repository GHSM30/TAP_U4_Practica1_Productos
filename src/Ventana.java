    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Producto productoInsertar = new Producto(jTextField1.getText(),Float.parseFloat(jTextField2.getText()),Integer.parseInt(jTextField3.getText()));
        boolean resultadoInsert = mySQL.insertar(productoInsertar);
        if(resultadoInsert){
            JOptionPane.showMessageDialog(this, "SE INSERTO CORRECTAMENTE EL DATO");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            actualizarJTable();
        }else{
            JOptionPane.showMessageDialog(this, "ERROR NO SE PUDO INSERTAR EL DATO");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int renglonSeleccionado = jTable1.getSelectedRow();
        if(renglonSeleccionado == -1){
            JOptionPane.showMessageDialog(this, "ERROR DEBES DE SELECCIONAR UN RENGLON PARA BORRAR UN DATO");
            return;
        }
        String idBorrar = tabla.getValueAt(renglonSeleccionado, 0).toString();
        boolean resultadoEliminar = mySQL.eliminar(idBorrar);
        int res = JOptionPane.showConfirmDialog(this, "ESTAS SEGURO DE ELIMINAR ESE RENGLON");
        if(res == JOptionPane.YES_OPTION){
                if(resultadoEliminar){
                actualizarJTable();
                JOptionPane.showMessageDialog(this, "EXITO SE BORRO EL DATO");
            }else{
                JOptionPane.showMessageDialog(this, "ERROR NO SE PUDO BORRAR EL DATO");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


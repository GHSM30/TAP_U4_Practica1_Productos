    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Producto productoActualizar = new Producto(jTextField5.getText(),Float.parseFloat(jTextField6.getText()),Integer.parseInt(jTextField7.getText()));
        productoActualizar.id = Integer.parseInt(jTextField4.getText());
        
        boolean respuestaActualizar = mySQL.actualizar(productoActualizar);
        if(respuestaActualizar){
            jTextField4.setEditable(true);
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            actualizarJTable();
            JOptionPane.showMessageDialog(this, "SE ACTUALIZO CORRECTAMENTE LA BD");
        }else{
            JOptionPane.showMessageDialog(this, "ERROR NO SE PUDO ACTUALIZAR LA BD");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

